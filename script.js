function FuseSearchTester() {
  const [query, setQuery] = React.useState("");
  const [options, setOptions] = React.useState({
      distance: 100,
      fieldNormWeight: 1,
      findAllMatches: true,
      ignoreFieldNorm: true,
      ignoreLocation: true,
      includeMatches: true,
      includeScore: true,
      isCaseSensitive: false,
      location: 0,
      minMatchCharLength: 1,
      shouldSort: true,
      threshold: 0.5,
      useExtendedSearch: true,
  });

  // 검색어를 Extended Search 패턴으로 변환하는 함수
  function createExtendedSearchPattern(searchTerm) {
    if (!searchTerm) return "";
    
    const lowercaseSearch = searchTerm.toLowerCase();
    let expandedTerms = [lowercaseSearch];
    
    // 검색어가 포함된 그룹의 모든 단어를 수집
    SEARCH_GROUPS.forEach(group => {
      if (group.some(term => term.toLowerCase().includes(lowercaseSearch))) {
        expandedTerms = [...new Set([...expandedTerms, ...group])];
      }
    });
    
    // Fuse.js Extended Search 패턴으로 변환
    // 각 단어를 정확히 매칭하도록 '=' 사용
    // OR 연산을 위해 '|' 사용
    return expandedTerms.map(term => `'${term}`).join(" | ");
  }

  const fuse = new Fuse(sampleData, {
      ...options,
      keys: [
          { name: "title", weight: 3 },
          { name: "overall", weight: 2 },
          { name: "story", weight: 1 }
      ],
  });

  // 확장된 검색 패턴을 사용하여 검색 실행
  const results = query ? fuse.search(createExtendedSearchPattern(query)) : [];


  return (
      <div className="p-6 max-w-7xl mx-auto">
          <h1 className="text-2xl font-bold mb-4">🔍 Fuse.js Search Tester</h1>
          
          <input
              type="text"
              className="input mb-4"
              placeholder="검색어를 입력하세요..."
              value={query}
              onChange={(e) => setQuery(e.target.value)}
          />

          <div className="grid grid-cols-4 gap-4 mb-4">
              {Object.entries(options).map(([key, value]) => (
                  <div key={key} className="mb-2">
                      <label className="text-xs font-semibold">{key}</label>
                      {typeof value === "boolean" ? (
                          <select
                              className="select"
                              value={value.toString()}
                              onChange={(e) => setOptions({ ...options, [key]: e.target.value === "true" })}
                          >
                              <option value="true">True</option>
                              <option value="false">False</option>
                          </select>
                      ) : (
                          <input
                              type="number"
                              className="input"
                              value={value}
                              onChange={(e) => setOptions({ ...options, [key]: parseFloat(e.target.value) || 0 })}
                          />
                      )}
                      <p className="text-xs text-gray-500">{optionDescriptions[key]}</p>
                  </div>
              ))}
          </div>

          <div className="results-container">
              <h2 className="text-sm font-semibold mb-2 sticky top-0 bg-white p-2">
                  🔎 검색 결과 ({results.length}개 찾음)
              </h2>
              <div className="grid grid-cols-4 gap-4">
                  {results.map(({ item, score }, index) => (
                      <div key={index} className="card">
                          <h3 className="font-bold">{item.title}</h3>
                          <p className="text-sm text-gray-500">{item.overall}</p>
                          <p className="text-sm text-gray-700">
                              {typeof item.story === "string" ? item.story.substring(0, 100) + "..." : ""}
                          </p>
                          <p className="text-sm text-gray-700">Score: {score.toFixed(3)}</p>
                      </div>
                  ))}
              </div>
          </div>
      </div>
  );
}

ReactDOM.render(<FuseSearchTester />, document.getElementById('root'));